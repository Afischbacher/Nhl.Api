namespace Nhl.Api.Models.Team;

/// <summary>
/// A model representing the NHL team colors for an NHL team
/// </summary>
public class TeamColors
{
    /// <summary>
    /// The primary color for the NHL team <br/>
    /// Example: #F47A38
    /// </summary>
    public string PrimaryColor { get; set; }

    /// <summary>
    /// The secondary color for the NHL team <br/>
    /// Example: #B9975B
    /// </summary>
    public string SecondaryColor { get; set; }

    /// <summary>
    /// The tertiary color for the NHL team <br/>
    /// Example: #C1C6C8
    /// </summary>
    public string TertiaryColor { get; set; }

    /// <summary>
    /// The quaternary color for the NHL team <br/>
    /// Example: #000000
    /// </summary>
    public string QuaternaryColor { get; set; }

    /// <summary>
    /// The quinary color for the NHL team <br/>
    /// Example: #FFD100
    /// </summary>
    public string QuinaryColor { get; set; }

    /// <summary>
    /// The senary color for the NHL team <br/>
    /// Example: #D18A00
    /// </summary>
    public string SenaryColor { get; set; }

    /// <summary>
    /// The septenary color for the NHL team <br/>
    /// Example: #001970
    /// </summary>
    public string SeptenaryColor { get; set; }
}
